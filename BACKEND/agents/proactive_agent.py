    def _add_alert(self, alert: ProactiveAlert):
        """Thread-safe alert addition. Uses Smart Interruption Logic."""
        # Smart Interruption Logic: skip or delay low priority if busy
        if self._user_context.get("is_busy") and alert.priority in ("low", "normal"):
            logger.info(f"Skipping or delaying non-urgent alert (user is busy): {alert.title}")
            # Optionally cache to deliver later. For now, silent default to prevent spam.
            if alert.priority == "low":
                return
            else:
                alert.priority = "delayed_normal"
        
        # Debounce/Cache check to avoid redundant alerts
        cache_key = f"{alert.alert_type}_{alert.title}"
        last_alert_time = self._cached_states.get(cache_key)
        if last_alert_time and (datetime.now() - last_alert_time).total_seconds() < 3600 * 4:
            # Found same alert in the past 4 hours.
            if alert.priority != "critical":
                return # Skip redundant repeated alert, keep it quiet
                
        self._cached_states[cache_key] = datetime.now()

        with self._lock:
            self._alerts.append(alert)
            if len(self._alerts) > self.MAX_ALERTS:
                self._alerts = self._alerts[-self.MAX_ALERTS:]

    def get_alerts(self, unseen_only: bool = False) -> List[Dict]:
        """Get all alerts, optionally only unseen ones."""
        with self._lock:
            alerts = self._alerts
            if unseen_only:
                alerts = [a for a in alerts if not a.seen]
            return [a.to_dict() for a in alerts]

    def mark_seen(self, index: int = -1):
        """Mark alerts as seen. -1 = mark all."""
        with self._lock:
            if index == -1:
                for a in self._alerts:
                    a.seen = True
            elif 0 <= index < len(self._alerts):
                self._alerts[index].seen = True

    def get_unseen_count(self) -> int:
        with self._lock:
            return sum(1 for a in self._alerts if not a.seen)

