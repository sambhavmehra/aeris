    def register_job(self, name: str, func: Callable, interval_seconds: int):
        """Register a new periodic job."""
        self._jobs[name] = ScheduledJob(name=name, func=func, interval_seconds=interval_seconds)

    def enable_job(self, name: str) -> bool:
        if name in self._jobs:
            self._jobs[name].enabled = True
            return True
        return False

    def disable_job(self, name: str) -> bool:
        if name in self._jobs:
            self._jobs[name].enabled = False
            return True
        return False

    # ═════════════════════════════════════════════════════════════════
    #  LIFECYCLE
    # ═════════════════════════════════════════════════════════════════

    def start(self):
        """Start the proactive agent background loop."""
        if self._running:
            return
        self._running = True
        self._thread = threading.Thread(target=self._run_loop, daemon=True)
        self._thread.start()
        logger.info("ProactiveAgent started")

    def stop(self):
        """Stop the proactive agent."""
        self._running = False
        logger.info("ProactiveAgent stopped")

    def _run_loop(self):
        """Main background loop — checks all jobs periodically with dynamic frequency."""
        while self._running:
            # Dynamic interval adjustment based on user context
            multiplier = 1.0
            if self._user_context.get("is_busy"):
                multiplier = 2.0  # Slow down background tasks if busy
            elif time.time() - self._last_active_time > 3600:
                multiplier = 0.5  # Give more resources to sensing if idle

            for name, job in self._jobs.items():
                # Check if job is due, considering the dynamic multiplier
                is_due = False
                if job.enabled:
                    if job.last_run is None:
                        is_due = True
                    else:
                        is_due = (datetime.now() - job.last_run).total_seconds() >= (job.interval_seconds * multiplier)

                if is_due:
                    try:
                        from engine.state_manager import global_state_manager
                        job_action_mapping = {
                            "system_health": "Sir, system monitor kar raha hoon...",
                            "download_watcher": "Sir, downloads check kar raha hoon...",
                            "critical_domain_monitor": "Sir, virus aur threats scan kar raha hoon...",
                            "integrity_check": "Sir, system files check kar raha hoon...",
                            "proactive_news": "Sir, latest updates check kar raha hoon..."
                        }
                        if name in job_action_mapping:
                            global_state_manager.set_global_action(job_action_mapping[name])
                    except Exception:
                        pass

                    try:
                        job.func()
                        job.last_run = datetime.now()
                        job.run_count += 1
                    except Exception as e:
                        logger.error(f"Proactive job '{name}' failed: {e}")
            
            try:
                from engine.state_manager import global_state_manager
                # Clear status only if we just ran a background task
                bg_statuses = [
                    "Sir, system monitor kar raha hoon...", 
                    "Sir, downloads check kar raha hoon...", 
                    "Sir, virus aur threats scan kar raha hoon...", 
                    "Sir, system files check kar raha hoon...",
                    "Sir, latest updates check kar raha hoon..."
                ]
                if global_state_manager.global_current_action in bg_statuses:
                    global_state_manager.set_global_action("Idle")
            except Exception:
                pass
            time.sleep(10)  # Poll every 10 seconds

    # ═════════════════════════════════════════════════════════════════
