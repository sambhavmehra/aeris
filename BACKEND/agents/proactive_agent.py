    def _job_system_health(self):
        """Check system health: CPU, memory, battery."""
        try:
            import psutil
            cpu_percent = psutil.cpu_percent(interval=2)
            memory = psutil.virtual_memory()
            mem_percent = memory.percent

            # Debounce rapid reporting
            prev_cpu = self._cached_states.get("prev_cpu", 0)
            if abs(cpu_percent - prev_cpu) < 10 and cpu_percent < 90:
                self._cached_states["prev_cpu"] = cpu_percent
                return
            self._cached_states["prev_cpu"] = cpu_percent

            alerts = []
            if cpu_percent > 90:
                alerts.append(f"CPU is high ({cpu_percent}%).")
            if mem_percent > 90:
                alerts.append(f"Memory is critical ({mem_percent}%).")

            # Battery check
            try:
                battery = psutil.sensors_battery()
                if battery and not battery.power_plugged and battery.percent < 20:
                    alerts.append(f"🔋 Battery low: **{battery.percent}%** — plug in soon!")
            except Exception:
                pass

            if alerts:
                self._add_alert(ProactiveAlert(
                    alert_type="health",
                    title="System Health",
                    message=" ".join(alerts) + " Want details?",
                    priority="high" if mem_percent > 90 or cpu_percent > 90 else "normal",
                    data={"cpu": cpu_percent, "memory": mem_percent},
                ))

        except ImportError:
            pass  # psutil not available
        except Exception as e:
            logger.error(f"Health check failed: {e}")

    def _job_disk_space(self):
        """Check disk space and warn if low."""
        try:
            import shutil
            if platform.system() == "Windows":
                drives = ["C:\\", "D:\\"]
            else:
                drives = ["/"]

            for drive in drives:
                try:
                    total, used, free = shutil.disk_usage(drive)
                    free_gb = free / (1024 ** 3)
                    used_pct = (used / total) * 100

                    if free_gb < 10:
                        prev_free = self._cached_states.get(f"disk_{drive}", 100)
                        if abs(prev_free - free_gb) < 1:
                            continue # Hasn't meaningfully changed
                        self._cached_states[f"disk_{drive}"] = free_gb

                        self._add_alert(ProactiveAlert(
                            alert_type="health",
                            title="Low Disk Space",
                            message=f"Only {free_gb:.1f}GB free on {drive}. Clean up?",
                            priority="high" if free_gb < 5 else "normal",
                            data={"drive": drive, "free_gb": round(free_gb, 1), "used_pct": round(used_pct, 1)},
                        ))
                except Exception:
                    pass
        except Exception as e:
            logger.error(f"Disk space check failed: {e}")

    def _job_download_watcher(self):
        """Watch Downloads folder for new files and suggest organization."""
        try:
            downloads_dir = Path.home() / "Downloads"
            if not downloads_dir.exists():
                return

            # Count unorganized files (top-level only)
            files = [f for f in downloads_dir.iterdir() if f.is_file() and not f.name.startswith(".")]

            if len(files) > 20:
                file_count = len(files)
                prev_count = self._cached_states.get("downloads_count", 0)
                if abs(file_count - prev_count) < 5:
                    return # Avoid redundant alerts
                self._cached_states["downloads_count"] = file_count
                
                recent = sorted(files, key=lambda f: f.stat().st_mtime, reverse=True)[:5]
                recent_names = [f.name for f in recent]

                self._add_alert(ProactiveAlert(
                    alert_type="suggestion",
                    title="Downloads Activity",
                    message=f"Downloads folder has {file_count} items. Organize them?",
                    priority="low",
                    data={"file_count": file_count, "recent": recent_names},
                ))

        except Exception as e:
            logger.error(f"Download watcher failed: {e}")

    def _job_memory_consolidation(self):
        """Consolidate chat history into compressed long-term facts."""
        try:
            from services.chat_engine import _load_chat_log
            messages = _load_chat_log()

            if len(messages) < 30:
                return  # Not enough history to consolidate

            # Extract key facts from recent conversations
            user_messages = [m["content"] for m in messages if m.get("role") == "user"][-20:]
            if not user_messages:
                return

            # Find patterns: frequently mentioned topics, apps, files
            from collections import Counter
            all_words = " ".join(user_messages).lower().split()
            # Filter out common words
            stop = {"the", "a", "an", "is", "are", "was", "were", "and", "or", "but", "in", "on",
                    "at", "to", "for", "of", "with", "what", "how", "why", "when", "where", "can",
                    "do", "does", "did", "i", "you", "me", "my", "your", "it", "this", "that",
                    "please", "thanks", "hi", "hello", "hey"}
            meaningful = [w for w in all_words if len(w) > 2 and w not in stop]
            top_topics = Counter(meaningful).most_common(10)

            if top_topics:
                # Compare cache to avoid repeating exact same insight
                topic_keys = tuple(sorted([t[0] for t in top_topics[:3]]))
                if self._cached_states.get("last_topics") == topic_keys:
                    return
                self._cached_states["last_topics"] = topic_keys

                topics_str = ", ".join(f"{word}" for word, count in top_topics[:3])
                self._add_alert(ProactiveAlert(
                    alert_type="briefing",
                    title="Memory Insight",
                    message=f"Detected pattern: frequent focus on {topics_str}. Want a summary?",
                    priority="low",
                    data={"top_topics": dict(top_topics)},
                ))

        except Exception as e:
            logger.error(f"Memory consolidation failed: {e}")

    def _job_context_awareness(self):
        """Analyzes active usage patterns to adjust intelligent states."""
        try:
            import psutil
            is_busy = False
            # Rough proxy for "user is busy": high CPU user process running or code editors open
            busy_processes = ["code", "idea", "studio", "zoom", "teams", "devenv", "node", "npm", "python"]
            for proc in psutil.process_iter(['name', 'cpu_percent']):
                try:
                    name = proc.info['name'].lower() if proc.info['name'] else ""
                    if any(b in name for b in busy_processes) and proc.info['cpu_percent'] > 5.0:
                        is_busy = True
                        break
                except (psutil.NoSuchProcess, psutil.AccessDenied):
                    pass
            
            if is_busy:
                self._last_active_time = time.time()
                self._user_context["is_busy"] = True
            else:
                # Idle if 10 mins passed since last busy state
                if time.time() - self._last_active_time > 600:
                    self._user_context["is_busy"] = False
        except Exception:
            self._user_context["is_busy"] = False

    def _job_critical_domain_monitor(self):
        """Simulate scanning critical domains based on context (e.g., dev/cybersecurity)."""
        # A lightweight example of event-driven scanning
        domain = self._user_context.get("domain", "development")
        
        if domain not in ["development", "cybersecurity"]:
            return
            
        try:
            import requests
            # Use public CIRCL CVE feed to get the latest CVEs
            response = requests.get("https://cve.circl.lu/api/last", timeout=10)
            if response.status_code == 200:
                cves = response.json()
                for cve in cves:
                    # Filter for only high-impact (cvss > 7.0 or marked critical)
                    cvss = cve.get('cvss', 0)
                    if cvss and float(cvss) >= 7.0:
                        cve_id = cve.get('id', 'Unknown CVE')
                        summary = cve.get('summary', '')[:100]
                        cache_key = f"cve_{cve_id}"
                        
                        if not self._cached_states.get(cache_key):
                            self._cached_states[cache_key] = True
                            self._add_alert(ProactiveAlert(
                                alert_type="suggestion",
                                title="Security Update",
                                message=f"Critical vulnerability detected ({cve_id}): {summary}... Want details?",
                                priority="high"
                            ))
                            break # Only alert once per run to avoid spam
        except Exception as e:
            logger.error(f"Failed to fetch CVE feed: {e}")

