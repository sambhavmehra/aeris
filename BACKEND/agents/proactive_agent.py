    def _job_proactive_news(self):
        """Proactively fetch and share 1-2 important news updates when user is idle."""
        if self._user_context.get("is_busy") or self._user_context.get("is_muted"):
            return
        
        idle_time = time.time() - self._last_active_time
        if idle_time < 120:
            return  # Only share news if user is idle for at least 2 minutes
        
        # Anti-spam: only share once per 30 minutes
        last_news_time = self._cached_states.get("last_news_time", 0)
        if time.time() - last_news_time < 1800:
            return
            
        results_titles = []
        import random
        
        try:
            from googlesearch import search
            
            topics = [
                "latest cybersecurity news today",
                "latest AI technology news today",
                "latest tech updates today",
            ]
            domain = self._user_context.get("domain", "development")
            if domain == "cybersecurity":
                topics = ["latest cybersecurity vulnerability news today", "latest hacking news today"]
            
            query = random.choice(topics)
            # Try Google Search first
            raw_results = list(search(query, advanced=True, num_results=3))
            
            for r in raw_results:
                results_titles.append(r.title)
                
        except Exception as e:
            logger.warning(f"Google Search failed (likely 429). Falling back to Hacker News: {e}")
            try:
                import requests
                # Fallback to Hacker News API
                resp = requests.get("https://hacker-news.firebaseio.com/v0/topstories.json", timeout=10)
                if resp.status_code == 200:
                    top_ids = resp.json()[:15]
                    random.shuffle(top_ids)
                    
                    for item_id in top_ids:
                        item_resp = requests.get(f"https://hacker-news.firebaseio.com/v0/item/{item_id}.json", timeout=5)
                        if item_resp.status_code == 200:
                            data = item_resp.json()
                            if data and "title" in data:
                                results_titles.append(data["title"])
                        if len(results_titles) >= 3:
                            break
            except Exception as e2:
                logger.error(f"Fallback news fetch also failed: {e2}")
                return

        if results_titles:
            shared_titles = self._cached_states.get("shared_news_titles", set())
            new_results = [t for t in results_titles if t not in shared_titles]
            
            if not new_results:
                return
                
            picked = new_results[:2]
            bullets = []
            for t in picked:
                bullets.append(f"• {t}")
                shared_titles.add(t)
                
            self._cached_states["shared_news_titles"] = shared_titles
            self._cached_states["last_news_time"] = time.time()
            
            news_text = "Latest tech headlines check kiye the:\n" + "\n".join(bullets)
            
            self._add_alert(ProactiveAlert(
                alert_type="briefing",
                title="Proactive News",
                message=news_text,
                priority="normal"
            ))

    def _job_integrity_check(self):
        """Self-monitoring system integrity check to locate dead files or broken connections."""
        try:
            import ast
            import pathlib
            
            backend_dir = pathlib.Path(__file__).parent
            py_files = list(backend_dir.glob("*.py"))
            
            module_imports = set()
            for pf in py_files:
                try:
                    tree = ast.parse(pf.read_text(encoding="utf-8"))
                    for node in ast.walk(tree):
                        if isinstance(node, ast.Import):
                            for n in node.names:
                                module_imports.add(n.name)
                        elif isinstance(node, ast.ImportFrom):
                            if node.module:
                                module_imports.add(node.module)
                except Exception:
                    pass
                    
            dead_modules = []
            for pf in py_files:
                stem = pf.stem
                if stem not in module_imports and stem not in ["api", "run", "setup", "__init__"]:
                    dead_modules.append(stem)
                    
            try:
                # Integrate with tools.py & core module registries to detect unregistered tools
                from tools.universal_registry import get_universal_registry
                from tools import tool_names
                reg_names = set(get_universal_registry().get_tool_names())
                t_names = set(tool_names())
                unregistered = t_names - reg_names
                for u in unregistered:
                    dead_modules.append(f"Unregistered Tool ({u})")
            except Exception:
                pass
                    
            if dead_modules:
                cache_key = f"dead_modules_{len(dead_modules)}"
                if not self._cached_states.get(cache_key):
                    self._cached_states[cache_key] = True
                    self._add_alert(ProactiveAlert(
                        alert_type="health",
                        title="Integrity Warning",
                        message=f"Detected system issues (unused files/unregistered tools): {', '.join(dead_modules[:3])}. Suggest fix or removal?",
                        priority="normal"
                    ))
        except Exception as e:
            logger.error(f"Integrity check failed: {e}")

