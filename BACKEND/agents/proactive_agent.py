        # System Status
        try:
            import psutil
            cpu = psutil.cpu_percent(interval=1)
            mem = psutil.virtual_memory()
            sections.append("### 💻 System Status")
            sections.append(f"- CPU: {cpu}% | RAM: {mem.percent}% ({mem.used // (1024**3)}GB / {mem.total // (1024**3)}GB)")
            try:
                battery = psutil.sensors_battery()
                if battery:
                    plug = "🔌 Plugged in" if battery.power_plugged else "🔋 On battery"
                    sections.append(f"- Battery: {battery.percent}% ({plug})")
            except Exception:
                pass
        except ImportError:
            sections.append("### 💻 System Status\n- *Install `psutil` for system monitoring*")

        # Disk Space
        try:
            import shutil
            total, used, free = shutil.disk_usage("C:\\" if platform.system() == "Windows" else "/")
            sections.append(f"- Disk: {free / (1024**3):.1f}GB free / {total / (1024**3):.0f}GB total")
        except Exception:
            pass

        # Downloads count
        try:
            dl = Path.home() / "Downloads"
            if dl.exists():
                count = sum(1 for f in dl.iterdir() if f.is_file())
                if count > 0:
                    sections.append(f"\n### 📥 Downloads\n- {count} files in Downloads folder")
        except Exception:
            pass

        # Pending alerts
        unseen = self.get_unseen_count()
        if unseen:
            sections.append(f"\n### 🔔 Notifications\n- {unseen} unseen alert(s)")

        sections.append(f"\n---\n*Generated at {now.strftime('%I:%M %p')}*")
        return "\n".join(sections)

